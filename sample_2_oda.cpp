#include <iostream>
using namespace std;

int main () {
	string cars[4] = {"Volvo", "BMW", "Ford", "Mazda"};
	//to display only the first value;
	cout << cars[0];
	
	//to display all values using loop;
	
	int y,z;
	for (y=0;y<z;y++) {
		cout <<cars [y]<<endl;
	}
	cout << "__________";
	return 0;
}
